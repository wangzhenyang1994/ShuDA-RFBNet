def test_net(save_folder, net, detector, cuda, dataset, transform, max_per_image,
             im_size=512, thresh=0.05):
    num_images = len(dataset)
    num_classes = 11
#    # all detections are collected into:
#    #    all_boxes[cls][image] = N x 5 array of detections in
#    #    (x1, y1, x2, y2, score)
    all_boxes = [[[] for _ in range(num_images)]
                 for _ in range(len(labelmap)+1)]
#
#    # timers
    _t = {'im_detect': Timer(), 'misc': Timer()}
    output_dir = get_output_dir('BDD_AP', set_type)
    det_file = os.path.join(output_dir, 'detections.pkl')
    h,w=720,1280
    scale = torch.Tensor([w, h, w, h]).cuda()

    for i in range(num_images):
        im, _ = dataset[i]        
        x = Variable(im.unsqueeze(0)).cuda()
        _t['im_detect'].tic()
        _, loc, conf = net(x)
        boxes, scores = detector.forward(loc, conf, priors)
        detect_time = _t['im_detect'].toc(average=False)
        boxes = boxes[0]
        scores=scores[0]
        boxes *= scale
        boxes = boxes.cpu().numpy()
        scores = scores.cpu().numpy()
        _t['misc'].tic()
        # skip j = 0, because it's the background class
        for j in range(1, num_classes):
            # dets = detections[0, j, :]
            inds = np.where(scores[:, j] > thresh)[0]
            if len(inds) == 0:
                all_boxes[j][i] = np.empty([0, 5], dtype=np.float32)
                continue
            c_bboxes = boxes[inds]
            c_scores = scores[inds, j]
            c_dets = np.hstack((c_bboxes, c_scores[:, np.newaxis])).astype(
                np.float32, copy=False)
            keep = nms(c_dets, 0.45, force_cpu=False)
            c_dets = c_dets[keep, :]
            all_boxes[j][i] = c_dets
        if max_per_image > 0:
            image_scores = np.hstack([all_boxes[j][i][:, -1] for j in range(1,num_classes)])
            if len(image_scores) > max_per_image:
                image_thresh = np.sort(image_scores)[-max_per_image]
                for j in range(1, num_classes):
                    keep = np.where(all_boxes[j][i][:, -1] >= image_thresh)[0]
                    all_boxes[j][i] = all_boxes[j][i][keep, :]
        nms_time = _t['misc'].toc()
        
        print('im_detect: {:d}/{:d} {:.3f}s'.format(i + 1,
                                                    num_images, detect_time))
        print('nms: {:d}/{:d} {:.3f}s'.format(i + 1,
                                                    num_images, nms_time))


    with open(det_file, 'wb') as f:
        pickle.dump(all_boxes, f, pickle.HIGHEST_PROTOCOL)

    print('Evaluating detections')
    evaluate_detections(all_boxes, output_dir, dataset)


